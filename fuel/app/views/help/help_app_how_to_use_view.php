  <div class="panel-heading"><h2 class="uc_bbs_title">アプリの使い方</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">アプリの使い方をスクリーンショット付きで解説します。</p>

	<p class="padding_top_20"><strong>1. ログインする</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/app_how_to_use_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/app_how_to_use_1.png" width="100%" style="max-width: 300px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">最初にアプリを起動するとログイン画面が表示されますので、ログインを行いましょう。Game Usersに以前にログインしたことがある方は、同じID＆パスワードでログインを行ってください。</p>


	<p class="padding_top_20"><strong>2. このデバイスで通知を受ける</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/app_how_to_use_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/app_how_to_use_2.png" width="100%" style="max-width: 300px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">初めてGame Usersアプリを利用したデバイス（スマートフォン、タブレット）の場合は、画像のように「このデバイスで通知を受けますか？」と尋ねられるので、OKを選んでください。OKを選ぶと現在使用している端末で、Game Usersからの通知を受け取ることができるようになります。</p>


	<p class="padding_top_20"><strong>3. お知らせを確認する</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/app_about_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/app_about_1.png" width="100%" style="max-width: 360px;"></a>
    <p class="padding_top_20">Game Usersからお知らせが来ている場合は、画像のように一覧で表示されます。これはブラウザでアクセスしたときに、上部メニューのベルをクリック（タップ）したときに表示されるお知らせと同じものです。</p>


  </div>

