  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-phone" aria-hidden="true"></span> アプリのおすすめポイント</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p><strong>1. 募集掲示板に返信が来ると通知が届きます</strong></p>
    <p class="padding_bottom_20 border_bottom_dashed">募集掲示板に投稿した際、返信があると通知で知らせてくれます。返信してくれた方にすぐに気づくことができるので、連絡が取りやすくなります。<br><br>例えばフレンド募集に返信してくれた方がいても、すぐに気づけなくて相手に申し訳ないことになってしまうこともあるでしょう。また定期的に掲示板を確認しに行かなければならないため、手間もかかります。<br><br>アプリを利用するとアプリの方から知らせてくれるので、すぐに応答することができ、定期的に掲示板を確認しにいく必要もありません。<br><br>募集を書きっぱなしにすることができるので、すごく楽です。</p>


	<p class="padding_top_20"><strong>2. 参加コミュニティに書き込みがあると通知が届きます</strong></p>
  <p>参加しているコミュニティの掲示板に誰かが書き込みを行うと、アプリが通知で知らせてくれます。この機能のおかげで、他の参加者と非常に交流がしやすくなります。その他にもコミュニティでなにか更新があったときにアプリが通知してくれるので、非常に便利です。</p>
  <p class="padding_top_20"><strong>アプリで通知を受けられるケース</strong><br><br>・ 掲示板に誰かが書き込む<br>・ 告知が追加・編集される<br>・ コミュニティの管理者がメンバーに向けて通知の一斉送信を行う</p>

