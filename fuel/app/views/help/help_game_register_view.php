<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-king" aria-hidden="true"></span> 登録方法</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">ゲームを登録する方法を解説していきます。ログイン必須です。</p>


    <p class="padding_top_20"><strong>1. まず登録したいゲームの名前を検索しましょう</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/game_register_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_register_2.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">すでに登録されているゲームは画像のように検索で出てきます。フォームの欄が灰色になっているのは登録されたゲームをGame Users運営が過去に確認した証です。確認済みの欄は編集が不可能になります。</p>


    <p class="padding_top_20"><strong>2. 存在していないゲームの場合は新規登録フォームが表示されます</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/game_register_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_register_3.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">オレンジ色の枠で囲まれた新規登録という文字が記載されている場合、新しくゲームを登録することができます。<br><br>※ 同じゲームがすでに登録されていないか、検索文字などを変更しながら、よく確認してください。</p>


    <p class="padding_top_20"><strong>3. 検索ワードを入力します</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_register_4.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_register_4.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">ゲーム名の下にある複数の欄は、検索ワードを入力する欄になっています。名前に関係するワードを入力してください。<br><br>画像の例では「ダークソウル4」を登録しようとしており、検索ワードに「ダクソ4（省略名）」「Dark Souls4（アルファベット表記）」を入力しています。ゲーム名に関係することだけで、登場人物などは入力しないようにしてください。検索ワードが存在しない、または思いつかない場合は未記入のままで構いません。<br><br>登録ボタンを押すとゲームが登録されます。</p>


	<p class="padding_top_20"><strong>4. ゲームページにアクセスしてみる</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_register_5.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_register_5.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20">ゲームページにアクセスする場合は、ブラウザでページをリロードしてからゲーム一覧ボタンを押してください。登録したばかりのゲームは、ここのフリガナ未設定行に入っています。<br><br>フリガナはGame Users運営が確認後に設定しています。<br><br>運営が設定する項目は、フリガナ、サムネイル、URLになります。URLは当初は以下のようにランダムな文字列が自動的に設定され、運営が確認後、正式なURLに置き換わります。URLをブラウザのお気に入りに入れたり、ブログなどに掲載する場合は気をつけてください。<br><br>ゲームの登録後、しばらく後に確認してもらうと正式なURLに変わっています。</p>

