<article class="panel panel-success">

  <div class="panel-heading"><h2 class="uc_bbs_title">ブラウザで通知を受ける方法</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">お使いのブラウザがGoogle Chrome、Firefoxの場合、ブラウザで通知を受けることができます。</p>


    <p class="padding_top_20"><strong>1. ログインする</strong></p>
    <p class="padding_bottom_20 border_bottom_dashed">まず上部メニューからログインしてください。</p>


    <p class="padding_top_20"><strong>2. プレイヤーページにアクセスする</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_about_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_about_3.png" width="100%" style="max-width: 448px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">ログイン後、プレイヤーページに自動的に移動しなかった場合は、上部メニューの「プレイヤー」をクリック（タップ）して、プレイヤーページに移動してください。</p>


    <p class="padding_top_20"><strong>3. 通知の許可をする</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/notification_browser_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/notification_browser_1.png" width="100%" style="max-width: 346px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">プレイヤーページにアクセスすると、通知の許可を求められるので許可してください。そのブラウザで初めてアクセスしたときに表示されます。2度目以降はこの画面は表示されません。</p>


    <p class="padding_top_20"><strong>4. 準備ができました</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_notification_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_notification_2.png" width="100%" style="max-width: 362px;"></a>
    <p class="padding_top_20">ブラウザで通知を受ける準備が整いました。通知が届いた場合、画像のような通知画面が表示されます。PCの場合はデスクトップに、スマートフォン・タブレットの場合は通知センターに表示されます。<br><br>通知がうまく受信できない場合は、プレイヤーページの通知設定を見直してみてください。</p>

  </div>

</article>


