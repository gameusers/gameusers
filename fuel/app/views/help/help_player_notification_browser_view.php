  <div class="panel-heading"><h2 class="uc_bbs_title">Google Chromeの場合</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">アドレスバーにこちらのアドレスをコピーして開くと、素早く設定できます。<br><a href="chrome://settings/contentExceptions#notifications" target="_blank">chrome://settings/contentExceptions#notifications</a></p>


    <p class="padding_top_20"><strong></strong></p>
    <a href="<?=URI_BASE?>assets/img/help/notification_browser_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/notification_browser_2.png" width="100%" style="max-width: 526px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">まずブラウザの「設定」を開いてください。そして画像の赤丸部分「詳細設定を表示…」をクリック（タップ）。</p>


    <a href="<?=URI_BASE?>assets/img/help/notification_browser_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/notification_browser_3.png" width="100%" style="max-width: 471px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">プライバシー欄にある「コンテンツの設定」を選んでください。</p>


    <a href="<?=URI_BASE?>assets/img/help/notification_browser_4.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/notification_browser_4.png" width="100%" style="max-width: 385px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">通知欄にある「例外の管理」を選んでください。</p>


    <a href="<?=URI_BASE?>assets/img/help/notification_browser_5.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/notification_browser_5.png" width="100%" style="max-width: 542px;"></a>
    <p class="padding_top_20">https://gameusers.org:443 と書かれている場所を探して許可を選択、後は完了ボタンを押すと設定は終わりです。</p>

  </div>

</article>


