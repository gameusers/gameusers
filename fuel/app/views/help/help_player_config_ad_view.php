<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title">広告コードの登録</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/help/player_config_ad_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/player_config_ad_3.png" width="100%" style="max-width: 432px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">Wikiに掲載する広告のコードを登録する場所です。ここで広告を登録すると、Wiki内の任意の場所に広告を掲載することができるようになります。<br><br>広告はGame Users運営によって確認され、アダルト広告や安全性が確認できないコードは掲載が認められないことがあります。確認済みの広告は「未承認」と書かれた部分が「承認済み」に変わります。掲載が認められなかった場合はコメント欄にその理由が記載されます。<br><br>登録した広告の貼り方については以下を参考にしてください。<br><br><a href="javascript:void(0)" class="bbs_list_title" onclick="GAMEUSERS.common.readHelp(this, 1, null, 'wiki_edit_ad')"><span class="glyphicon glyphicon-question-sign" aria-hidden="true"></span> Wiki > 編集 ： 広告を貼る</a></p>


	<p class="padding_top_20"><strong>広告名</strong></p>
    <p>Wikiから呼び出すときに必要になります。1文字以上、20文字以内。利用できる文字は半角英数字とハイフン( - )アンダースコア( _ )です。例）ad_1</p>

	<p class="padding_top_20"><strong>広告コード</strong></p>
    <p>アフィリエイト提供会社（Google Adsense、Amazonアソシエイトなど）から取得できる広告コードを貼り付けてください。PCでアクセスしたとき、スマートフォンでアクセスしたとき、それぞれ違う広告を表示することができます。例）PCの場合は横幅の大きな広告を表示、スマートフォンの場合は横幅の小さな広告を表示。</p>

	<p class="padding_top_20"><strong>コメント欄</strong></p>
    <p>自分用に広告について解説を残しておきたい場合などに使用してください。</p>

	<p class="padding_top_20"><strong>自分がアクセスした時は表示しない</strong></p>
    <p>このチェックボックスは、広告の誤クリックを防止するためにあります。クリック型広告を掲載する場合はチェックしておきましょう。Game Usersにログインしている間、チェックした広告は自分に対して表示されません。</p>

  </div>

</article>
