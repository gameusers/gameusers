<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title">メンバー</h2></div>
  <div class="panel-body" id="bbs_thread_box">


    <a href="<?=URI_BASE?>assets/img/help/community_member_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_member_2.png" width="100%" style="max-width: 458px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">メンバータブでは、コミュニティに参加しているメンバーを退会させる、BANする、モデレーターに認定することができます。</p>


    <p class="padding_top_20"><strong>コミュニティから退会させる</strong></p>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">各メンバープロフィール付近に表示されている、退会ボタンを押すと、「このメンバーを退会させますか？」と尋ねられます。そこでOKを押すとそのメンバーは退会させられます。誤ってコミュニティに参加させてしまった場合などに利用してください。<br><br>この方法で退会させた場合、そのユーザーは再度コミュニティに参加することができます。二度とコミュニティに参加させたくないユーザーがいる場合はこちらではなく「BAN」を選択してください。</p>


    <p class="padding_top_20"><strong>コミュニティからBANする</strong></p>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">各メンバープロフィール付近に表示されている、BANボタンを押すと、「このユーザーをBANしますか？」と尋ねられます。そこでOKを押すとそのメンバーはコミュニティからBANされます。BANされると、そのユーザーはコミュニティから退会させられ、BANが解除されるまで、コミュニティに参加することができなくなります。<br><br>BANを解除したい場合は、BAN解除ボタンから解除してください。</p>


    <p class="padding_top_20"><strong>モデレーターに認定する</strong></p>
    <p class="padding_top_20">各メンバープロフィール付近に表示されている、モデレーター認定ボタンを押すと、「モデレーターに認定しますか？」と尋ねられます。そこでOKを押すと、そのメンバーはコミュニティのモデレーターに認定されます。<br><br>モデレーターというのは、コミュニティの管理者が特別な権限を与えることができるメンバーで、掲示板の削除権限を与えたり、メンバーの承認・退会権限を与えたり、通知の一斉送信を行う権限を与えたりすることが可能になります。モデレーターにどういった権限を与えるかは、設定 > 閲覧権限・操作権限で、管理者だけが設定することができます。<br><br>一人でコミュニティを管理するのが大変な場合などに、特定のメンバーにお願いしてモデレーターになってもらうといいでしょう。</p>


  </div>

