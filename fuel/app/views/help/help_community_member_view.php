  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/help/community_member_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_member_1.png" width="100%" style="max-width: 458px;"></a>
    <p class="padding_top_20">コミュニティへの参加方法が「承認後に参加」になっているコミュニティは、管理者（モデレーター）がユーザーの参加申請を承認して初めてコミュニティへの参加が許されます。<br><br>メンバータグ > メンバー承認<br><br>こちらでメンバーの承認が行えますので、コミュニティの管理者（モデレーター）は定期的にメンバータブを確認して、参加申請が来ていないかチェックしましょう。参加申請が来ている場合は「メンバー承認」ボタンの数字が増えます。<br><br>各ユーザーのプロフィール近くに表示されている承認ボタンを押すと、「このユーザーをコミュニティのメンバーに加えますか？」と尋ねられるので、承認する場合はOKを押してください。<br><br>※ 参加方法が「誰でも参加」になっているコミュニティの場合、メンバー承認ボタンは表示されません。</p>

