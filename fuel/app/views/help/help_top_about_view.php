  <div class="panel-heading"><h2 class="uc_bbs_title">Game Usersとは？</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/social/ogp_image.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/social/ogp_image.jpg" width="100%"></a>
    <p class="padding_top_20"><strong>Game Usersとは？</strong></p>
    <p class="padding_bottom_20 border_bottom_dashed">Game Users（ゲームユーザーズ）はゲームユーザーのためのSNS・コミュニティサイトです。ゲームに興味のある人たちが集まって、交流がしやすくなるような様々な機能を用意しています。</p>


    <p class="padding_top_20"><strong>コンテンツについて</strong></p>
    <p class="padding_bottom_20 border_bottom_dashed">Game Usersが現在提供している基本的なコンテンツ（ページ）は、ゲームページ、コミュニティ、Wiki、プレイヤーの4つです。</p>


    <p class="padding_top_20"><span class="glyphicon glyphicon-tower" aria-hidden="true"></span> <strong>ゲームページ</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_gameusers_1.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_gameusers_1.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">各ゲームに関する掲示板を利用することができ、ユーザー同士で交流することが可能です。誰でもスレッドが立てられ、どんな話題でも書き込める交流掲示板と、フレンド募集、ギルド・クランメンバー募集などが行える募集掲示板の2種類を利用することができます。<br><br>現在Game Usersに載ってないゲームでも自由に登録することができ、コンシューマーゲーム、PCゲーム、スマホゲームなど、どんなジャンルのゲームでも登録することが可能です。（アダルト・18禁ゲームは除く）</p>


    <p class="padding_top_20"><span class="glyphicon glyphicon-leaf" aria-hidden="true"></span> <strong>コミュニティ</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_gameusers_2.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_gameusers_2.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">イベントを告知したり、掲示板を使ってメンバーと交流したり、メンバー全員にメール・アプリの通知を一斉送信したりすることができるようになるコミュニティを作成することができます。コミュニティ内の情報は誰でも見れるようにしたり、参加メンバーしか見れないようにしたり、公開範囲を自由に設定することが可能です。<br><br>ギルド・クランのメンバー交流用コミュニティや、ゲーム配信者の配信用コミュニティなどに活用してください。</p>


    <p class="padding_top_20"><span class="glyphicon glyphicon-book" aria-hidden="true"></span> <strong>Wiki</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_gameusers_3.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_gameusers_3.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">みんなで記事を編集してコンテンツを充実させていけるWikiを作成することができます。ゲームの攻略Wikiなどに利用してください。<br><br>作成したWikiには広告を掲載することができるので、そこから収益を得ることも可能です。</p>


    <p class="padding_top_20"><span class="glyphicon glyphicon-user" aria-hidden="true"></span> <strong>プレイヤー</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_gameusers_4.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_gameusers_4.jpg" width="100%"></a>
    <p class="padding_top_20">自分のプロフィールを掲載することができます。またゲームごとにプロフィールを作ることもできるので、どんなゲームをプレイしているのか相手に認識してもらいやすくなります。</p>

