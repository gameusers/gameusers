  <div class="panel-heading"><h2 class="uc_bbs_title">デフォルト広告の設定</h2></div>
  <div class="panel-body" id="bbs_thread_box">


    <a href="<?=URI_BASE?>assets/img/help/wiki_edit_ad_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/wiki_edit_ad_2.png" width="100%" style="max-width: 436px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">デフォルト広告を設定すると、Wikiのすべてのページに広告を掲載することができるようになります（ページごとに広告を貼る必要がなくなります）。デフォルト広告に設定できるのはWikiのページ最上部（横長の広告）と下部のレクタングル広告（四角い広告の左側）の二箇所です。<br><br>ここで選択できる広告は、プレイヤーページの広告設定で登録した広告です。広告を登録していない場合は、まず広告コードを登録する必要があります。</p>


    <p class="padding_top_20"><strong>広告について</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/wiki_edit_ad_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/wiki_edit_ad_3.png" width="100%" style="max-width: 300px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">初期状態ではGame Usersが用意している広告が3つ貼り付けられています。すべてGoogle Adsenseの広告で、画像の広告1～3の場所にあります。デフォルト広告で置き換えられるのは、このうちの2つで「ページ最上部」と「ページ下部（左）」の広告になります。ページ下部（右）の広告は、Game Usersのものがそのまま表示され続けます。</p>


    <p class="padding_top_20"><strong>広告の推奨サイズ</strong></p>
    <p class="padding_top_20">「ページ最上部」は横長の広告、「ページ下部」は四角いレクタングル広告で、サイズは300 × 250のものを推奨しています。</p>


  </div>

</article>


