  <div class="panel-heading"><h2 class="uc_bbs_title">Google Adsenseについて</h2></div>
  <div class="panel-body" id="bbs_thread_box">


    <p>Game Usersが用意しているGoogle Adsense広告は、どのページにも必ず1つは掲載されます。Google Adsenseのポリシーでは1ページに3つまでの広告ユニットしか掲載できないと定められていますので、もしGoogle Adsense広告を掲載したい場合は、1つのページに2つの広告ユニットまでにしてください。<br><br>リンクユニットや検索ボックスなどはポリシーの制限内で自由にご利用いただけます。</p>


