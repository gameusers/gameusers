  <div class="panel-heading"><h2 class="uc_bbs_title">その他の広告の貼り方</h2></div>
  <div class="panel-body" id="bbs_thread_box">


    <p><strong>#gu_ad(広告名)</strong><br><br>プレイヤーページで登録した広告は、Wikiのページを編集して広告を貼りたい部分に上記のコードを貼り付けることで表示することができます。<br><br>例えば、広告名がad_1の場合は　#gu_ad(ad_1)　となります。</p>


  </div>

</article>


