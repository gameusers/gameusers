  <div class="panel-heading"><h2 class="uc_bbs_title">基本設定</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/help/community_config_basic_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_config_basic_1.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">設定タブ > 基本設定<br><br>ここではコミュニティの名前や説明文など、そういった基本的な設定を変更することができます。</p>
	<p class="padding_top_20"><strong>変更可能な項目</strong><br><br>
		<strong>・ コミュニティの名前</strong><br>
		50文字以内でコミュニティの名前を入力してください。<br><br>

		<strong>・ コミュニティの説明文</strong><br>
		コミュニティの説明文です。参加する人がコミュニティについて理解しやすいような説明文にしてください。3000文字以内。<br><br>

		<strong>・ コミュニティの説明文（一覧用）</strong><br>
		Game Usersのトップページなどで表示されるコミュニティ一覧用の短い説明文です。100文字以内。<br><br>

		<strong>・ トップ画像</strong><br>
		コミュニティページのトップに表示される画像です。アップロードできる画像の種類はJPEG、PNG、GIF、BMPで、ファイルサイズが3MB以内のものです。<br><br>

		<strong>・ サムネイル画像</strong><br>
		コミュニティ一覧で、コミュニティ情報と共に表示される小さな画像です。アップロードされた画像は自動的に正方形にリサイズされます。アップロードできる画像の種類はJPEG、PNG、GIF、BMPで、ファイルサイズが3MB以内のものです。<br><br>

		<strong>・ コミュニティID</strong><br>
		コミュニティIDはコミュニティのURLとして使われます。https://gameusers.org/uc/コミュニティID<br>利用できる文字は半角英数字（アルファベット大文字禁止）とハイフン( - )アンダースコア( _ )です。3文字以上、50文字以内。<br><br>

		<strong>・ 関連ゲーム</strong><br>
		コミュニティに関連のあるゲームを選んでください。10個までゲームを登録できますが、最も関連の深いゲームは一番最初に登録してください。コミュニティ一覧では、最初に選択された1件のゲーム名しか表示されません。<br><br>

		下の欄にゲーム名を入力するとゲームを検索できます。目当てのゲームが検索しても出てこない場合は、Game Usersトップページで「ゲーム登録」を選んで、登録してください。
	</p>

  </div>

