  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-book" aria-hidden="true"></span> Wikiのおすすめポイント</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">Game Users Wikiのおすすめポイントを紹介します。</p>


    <p class="padding_top_20"><strong>1. 広告を貼って収益化できる</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/wiki_about_1.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/wiki_about_1.jpg" width="100%" style="max-width: 540px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">AmazonアソシエイトのトラッキングIDを貼るだけで、画像のようなAmazonの広告が自動的に生成されます。またGoogle Adsenseなど、アフィリエイトサイトが用意している広告を貼ることも可能です。<br><br>※ ただし安全性の確認できない広告、アダルト広告の掲載は禁止しています。</p>


    <p class="padding_top_20"><strong>2. スマートフォンに対応</strong></p>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">PCからのアクセスだけでなく、スマートフォンやタブレットからのアクセスにも対応しています。携帯用端末でアクセスすると自動的に表示が変わります。</p>


    <p class="padding_top_20"><strong>3. YouTubeの動画が簡単に貼り付けられます</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/wiki_about_2.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/wiki_about_2.jpg" width="100%" style="max-width: 330px;"></a>
    <a href="<?=URI_BASE?>assets/img/help/wiki_about_3.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/wiki_about_3.jpg" width="100%" class="padding_top_20"></a>
    <p class="padding_top_20">Game Usersが用意している専用のプラグインを使うことで、YouTubeの動画がサムネイルつきで簡単に貼り付けられます。<br><br>動画のサムネイルをクリック（タップ）すると、動画が表示されます。</p>


