  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">コミュニティから退会する方法を解説します。</p>


    <p class="padding_top_20"><strong>1. 退会するボタンを押しましょう</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_join_4.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_join_4.png" width="100%"></a>
    <p class="padding_top_20">コミュニティページの情報タブを押すと、「退会する」ボタンが掲載されている画面が表示されます。<br><br>退会するボタンを押すと「コミュニティを退会しますか？」と意思確認が行われますので、そこでOKを選んでください。それでコミュニティからの退会が完了します。</p>


