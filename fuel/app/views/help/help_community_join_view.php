  <div class="panel-heading"><h2 class="uc_bbs_title">コミュニティに参加する</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">コミュニティに参加する方法を解説します。</p>


    <p class="padding_top_20"><strong>1. ログインしていない場合</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_join_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_join_1.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">コミュニティページの参加タブを押すと、「参加する」ボタンが掲載されている画面が表示されます。ログインしていない場合はここで参加ボタンを押すと、ログインページに移行しますので、そこでログインを行いましょう。ログイン行うと再度コミュニティのページに戻ってきますので、参加タブをもう一度押してください。</p>


    <p class="padding_top_20"><strong>2. ログインしている場合</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_join_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_join_2.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">コミュニティに参加するプロフィールを選んでください。複数プロフィールを作成している場合は、画像のように複数のプロフィールから選択することが可能になります。ここで選んだプロフィールはコミュニティのメンバー一覧に表示され、掲示板に書き込む際にも利用されます。プロフィールは後から変更することもできるので、気軽に選んでもらってかまいません。<br><br>後は「参加する」ボタンを押すだけで、すぐにコミュニティのメンバーになれます。</p>


    <p class="padding_top_20"><strong>3. 管理者の承認後に参加できるコミュニティ</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/community_join_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_join_3.png" width="100%"></a>
    <p class="padding_top_20">コミュニティの参加方法には「誰でも参加」と「承認後に参加」の2種類のタイプがあります。誰でも参加の場合は、ログインしてから参加するボタンを押すとすぐに参加できますが、承認後に参加の場合は、コミュニティの管理者が参加申請した人をチェックしてから、参加させるかどうかを決めることになります。<br><br>画像のように「承認後に参加」と書かれているコミュニティの場合は、参加申請をしてからコミュニティの管理者が承認してくれるまで、しばらく待っていてください。</p>


  </div>

