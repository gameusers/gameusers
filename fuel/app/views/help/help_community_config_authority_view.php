  <div class="panel-heading"><h2 class="uc_bbs_title">操作権限</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/help/community_config_auth_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_config_auth_2.png" width="100%"></a>
    <p class="padding_top_20">設定タブ > 操作権限<br><br>ここではコミュニティ内のコンテンツを操作できる人を決めることができます。例えば管理者だけが掲示板のスレッドを立てられるようにしたい場合は、掲示板部分のすべてのチェックを外してください。そうすると一般ユーザーもコミュニティメンバーもモデレーターもスレッドが立てられなくなります。<br><br>モデレーターというのは、コミュニティの管理者（あなた）が特別な権限を与えることができるメンバーで、掲示板の削除権限を与えたり、メンバーの承認・退会権限を与えたり、通知の一斉送信を行う権限を与えたりすることが可能になります。<br><br>コミュニティ内の重要な操作に関する権限はモデレーターにのみ与えることができます。通常の参加メンバーには大きな権限を与えることはできません。複数人でコミュニティを管理したい場合はメンバータブからモデレーターを認定してください。<br><br>※ 操作権限はコミュニティの管理者しか編集することはできません。</p>

  </div>

</article>
