    <p class="padding_top_20">設定タブ > 閲覧権限<br><br>ここではコミュニティ内のコンテンツを閲覧できる人を決めることができます。例えばコミュニティに参加しているメンバーを部外者の人に見せたくない場合は、メンバーの部分の一般ユーザーのチェックを外してください。そうすると、コミュニティに参加している人だけが他のメンバーを確認することができるようになります。<br><br>一般ユーザーとは参加メンバー、モデレーター、管理者を除くすべての人たちのことです。<br><br>知り合いや身内の人たちだけで交流をしたい場合は、一般ユーザーのチェックをすべて外してください。部外者の人間はコミュニティ内のコンテンツを一切覗くことができなくなります（ただしコミュニティの説明文はすべての人が見ることができます）。逆に誰でも参加できるオープンなコミュニティにしたい場合は、一般ユーザーのチェックをすべてチェックしたままにしておいてください。<br><br>※ 閲覧権限はコミュニティの管理者しか編集することはできません。</p>

