  <div class="panel-heading"><h2 class="uc_bbs_title">ログインとは？</h2></div>
  <div class="panel-body" id="bbs_thread_box">


    <a href="<?=URI_BASE?>assets/img/help/about_login_1.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_login_1.jpg" width="100%"></a>
    <p class="padding_top_20">Game Usersにログインすると、以下の機能が使えるようになります。</p>

    <ul class="padding_top_10">
      <li>プレイヤーページを利用可能</li>
      <li>コミュニティに参加、作成することができる</li>
      <li>Wikiを作成できる</li>
      <li>掲示板の書き込みをいつでも編集可能</li>
      <li>募集掲示板への書き込みに返信が来た場合、メールやアプリで通知を受けることができる</li>
    </ul>

    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">また通知は、募集掲示板の返信からだけでなく、参加したコミュニティからも受け取ることができるようになります。例えば、ギルドやクラン用コミュニティからのメンバーへの一斉連絡、ゲーム配信者コミュニティからの配信開始通知などです。<br><br>このように様々な特典があるので、Game Usersはログインしてから利用することをおすすめします。</p>


	<p class="padding_top_20"><strong>ログインの仕方</strong></p>
	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">Game Usersにログインする際に、Twitter、Yahoo、Googleなどの大手サイトのアカウントを利用することができます。すでに所有しているアカウントを利用できるので、IDやパスワードの管理が楽になります。<br><br>大手サイトのアカウントを利用しない場合は、IDとパスワードを入力して、新たにアカウントを作成することも可能です。</p>


    <p class="padding_top_20"><strong>プレイヤーページについて</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_gameusers_4.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_gameusers_4.jpg" width="100%"></a>
    <p class="padding_top_20">ログインするとユーザーごとにプレイヤーページが利用できるようになります。このページはゲームに関するプロフィールを掲載する場所で、ここで自分のプロフィールを作成しておくと、募集掲示板に書き込む際に特定のプロフィールを利用して募集をかけることができるようになります。<br><br>例えば、自分がバトルフィールドをプレイしている場合、バトルフィールド用のプロフィールを作成することができ（プレイスタイルやキルレートなどを掲載）、それを利用して募集をかけると、自分がどんなプレイヤーなのかが相手に伝わりやすくなります。この機能を利用すると、募集文に自分についての紹介文などを長々と書く必要がなくなるので、フレンド募集やメンバー募集をする際に非常に便利です。</p>

