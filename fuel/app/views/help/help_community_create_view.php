		<strong>・ コミュニティの説明文</strong><br>コミュニティの説明文です。参加する人がコミュニティについて理解しやすいような説明文にしてください。3000文字以内。<br><br>
		<strong>・ コミュニティの説明文（一覧用）</strong><br>Game Usersのトップページなどで表示されるコミュニティ一覧用の短い説明文です。100文字以内。<br><br>
		<strong>・ コミュニティID</strong><br>コミュニティIDはあなたのコミュニティのURLとして使われます。　https://gameusers.org/uc/コミュニティID<br>利用できる文字は半角英数字（アルファベット大文字禁止）とハイフン( - )アンダースコア( _ )です。3文字以上、50文字以内。<br><br>
		<strong>・ 関連ゲーム</strong><br>作成するコミュニティに関連のあるゲームを選んでください。10個までゲームを登録できますが、最も関連の深いゲームは一番最初に登録してください。Game Usersのトップページなどで表示されるコミュニティ一覧では、最初に選択された1件のゲーム名しか表示されません。<br><br>下の欄にゲーム名を入力するとゲームを検索できます。目当てのゲームが検索しても出てこない場合は、Game Usersトップページでゲーム登録を行ってください。
	</p>


