  <div class="panel-heading"><h2 class="uc_bbs_title">アカウントを作成する</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">Game Usersにログインする場合、TwitterやGoogleなどの大手サイトのアカウントを利用する方法と、Game Usersでアカウントを作成してからログインする方法の2つが用意されています。<br><br>大手サイトのアカウントを利用しない場合は、こちらの方法でアカウントを作成してからログインしてください。</p>

    <p class="padding_top_20"><strong>1. ログインページに移動します</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/account_create_1.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/account_create_1.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">上部メニューの「ログイン」を押します。</p>


    <p class="padding_top_20"><strong>2. アカウント作成ボタンを押す</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/account_create_2.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/account_create_2.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">ページ上部にある「アカウント作成」と書かれているボタンを押してください。</p>


    <p class="padding_top_20"><strong>3. ID、パスワードを入力する</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/account_create_3.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/account_create_3.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">IDとパスワードを入力してから、赤丸の「アカウント作成」ボタンを押してください。利用できる文字列は半角英数字とハイフン( - )アンダースコア( _ )です。<br><br>※ IDは3文字以上、25文字以内。パスワードは6文字以上、32文字以内。</p>


    <p class="padding_top_20"><strong>4. アカウント作成完了！</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/account_create_4.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/account_create_4.jpg" width="100%"></a>
    <p class="padding_top_20">この画面に移動すると、アカウントの作成が完了したことになります。（作成したアカウントでログインしている状態になっています）<br><br>この画面はプレイヤーページというページで、自分のアカウントのホーム画面になります。上部メニューにある「プレイヤー」というリンクを押すと、いつでもここに戻ってくることが可能です。プロフィールを入力したり、画像をアップロードして、情報を充実させてみましょう！</p>

  </div>

