  <div class="panel-heading"><h2 class="uc_bbs_title">プロフィールを追加する</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/help/player_profile_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/player_profile_3.png" width="100%" style="max-width: 528px;"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">プレイヤーページの下部にある「プロフィールを追加する」ボタンを押すと、プロフィールを追加することができます。メインのプロフィールと基本的な項目は同じですが、追加プロフィールの場合は「プロフィールの公開・非公開」、「関連ゲーム」、これらの項目がフォームに追加されています。</p>


	<p class="padding_top_20">
		<strong>フォームの追加内容</strong><br><br>
		<strong>・ プロフィールの公開・非公開</strong><br>例えば、他の人に隠れて参加したいコミュニティがある場合、非公開にしたプロフィールで参加することができます。他のユーザーは、非公開プロフィールで参加したコミュニティから当ページにたどり着くことはできません。また非公開にしたプロフィールは、プレイヤーページ内に表示されることもありません（第三者からは見えません）。<br><br>
		<strong>・ 関連ゲーム</strong><br>このプロフィールに関連するゲームを選択してください。ゲームは10個まで選択できます。下の欄にゲーム名を入力するとゲームを検索できます。目当てのゲームが検索しても出てこない場合は、Game Usersトップページにあるゲームタブから「登録」を選んで、ゲームを登録してください。
	</p>


  </div>

