<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-leaf" aria-hidden="true"></span> コミュニティのおすすめ機能</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">ブラウザ・アプリ・メールでの通知機能が非常に優秀です！</p>


    <p class="padding_top_20"><strong>1. 高機能な掲示板を利用できます</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_about_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_about_2.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">スレッドを自由に立てられ、2MBまでの画像やYouTubeの動画を、コメント内に貼り付けられる掲示板を利用することができます。</p>


    <p class="padding_top_20"><strong>2. 掲示板に書き込みがあるとすぐにわかります！</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/community_about_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_about_3.png" width="100%" style="max-width: 448px;"></a>
    <a href="<?=URI_BASE?>assets/img/help/community_about_4.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_about_4.png" width="100%" class="padding_top_20"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">掲示板に書き込みがあると、上部メニューのベルの数字が増えてすぐにわかるようになっています。最新版のGoogle Chrome、またはFirefoxを使っている方はブラウザに通知が届き、アプリをインストールしている方はアプリの通知が届きます。書き込みに気付きやすくなるシステムになっているので、メンバー同士の連絡が取りやすいです。</p>


    <p class="padding_top_20"><strong>3. コミュニティの管理者は参加メンバーに一斉に通知を送信できます</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/community_about_5.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_about_5.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">コミュニティの参加メンバーになにか知らせたいことがある場合、ブラウザの通知機能、アプリ、またはメールを利用して全員に対してメッセージを送ることが可能です。Game Usersの機能を利用するとお互いのメールアドレスを交換する必要はなく、アプリでお互いにフォローしあったりする必要もありません。コミュニティに参加しているだけで、管理者からのメッセージを受け取ることができるようになります。<br><br>例えばギルドのコミュニティを作った場合、連絡事項をメンバー全員に送ったり、ゲーム内で集まってクエストをする際などに、簡単に呼びかけを行うことができるのです。<br><br>通知の一斉送信はコミュニティの管理者だけでなく、管理者が選んだモデレーター（特別な権限を持ったユーザー）も送ることが可能です。</p>


	<p class="padding_top_20"><strong>4. 身内だけのコミュニティも作れます</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/community_about_6.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_about_6.png" width="100%"></a>
    <p class="padding_top_20">誰でも参加できるオープンなコミュニティと、身内だけが参加できるクローズドなコミュニティの両方を作ることが可能です。コミュニティの権限は細かく設定することができるので、誰でも掲示板を見ることはできるが、書き込めるのは参加メンバーだけといった設定にすることも可能です。</p>


  </div>

