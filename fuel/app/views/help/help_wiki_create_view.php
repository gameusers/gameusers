		<strong>・ Wikiの名前</strong><br>Wikiの名前を入力してください。3文字以上、50文字以内。<br><br>
		<strong>・ Wikiの説明文</strong><br>Wikiの説明文です。名前と共にWiki一覧に表示されます。100文字以内。<br><br>
		<strong>・ 管理者パスワード</strong><br>Wikiの編集を凍結したり、アップロードされた画像を消去するときに、管理者パスワードが必要になります。半角英数字を利用して入力してください。6文字以上、32文字以内。WikiのURLと同じ文字列は使用できません。<br><br>
		<strong>・ 関連ゲーム</strong><br>作成するWikiに関連のあるゲームを選んでください。フォームにゲーム名を入力するとゲームを検索できます。目当てのゲームが検索しても出てこない場合は、Game Usersトップページで「ゲーム登録」を選んで、登録してください。
