<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-pencil" aria-hidden="true"></span> ログインした場合のメリット</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">ログインしてから掲示板を利用すると、いろいろとメリットが増えます。</p>


    <p class="padding_top_20"><strong>1. 毎回ハンドルネームを入力する手間がなくなります</strong></p>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">プレイヤーページで作ったプロフィールが自動的に使用され、プロフィールへのリンクも掲載されるようになります。Game Usersでは1つのアカウントで複数のプロフィールを作成することができるので、掲示板に書き込む際に使用するプロフィールをその中から選択することができます。<br><br>この機能を利用すると、ゲームごとに書き込むプロフィールを変更することができるのです。</p>


    <p class="padding_top_20"><strong>2. 書き込み内容の編集をいつでも行えます</strong></p>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">ログインせずに書き込んだコメントは30分が経過すると編集できなくなるのですが、ログインして書き込んだコメントはいつでも編集が可能です。誤字脱字が多い人でも安心！</p>


    <p class="padding_top_20"><strong>3. 匿名でも書き込めます</strong></p>
    <p>ログインせずにハンドルネーム欄を未入力のままコメントを投稿すると、「ななしさん」として匿名で書き込むことができますが、ログインしている場合でもコメントの書き込みフォームで「ななしにする チェックボックス」をチェックすると、匿名のななしさんとして書き込むことが可能です。<br><br>ハンドルネームが「ななしさん」になり、自分のプロフィールへのリンクは表示されません。さらに編集をいつでも行えるというメリットもついています。</p>


  </div>

