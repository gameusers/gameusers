  <div class="panel-heading"><h2 class="uc_bbs_title">コミュニティ参加</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p>コミュニティの参加方法を決めます。「誰でも参加」と「承認後に参加」の2種類が選べます。<br><br>「誰でも参加」はログインしているメンバーなら誰でもコミュニティに参加することができます。<br><br>「承認後に参加」は参加申請が来たときに、コミュニティの管理者（モデレーター）が承認するかどうかを決め、承認されたユーザーだけがコミュニティに参加することができます。<br><br>オープンなコミュニティにしたい場合は「誰でも参加」を、身内だけやクローズドなコミュニティにしたい場合は「承認後に参加」を選ぶといいでしょう。</p>

  </div>

</article>


<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title">オンライン扱いになる時間</h2></div>
  <div class="panel-body" id="bbs_thread_box">

	<a href="<?=URI_BASE?>assets/img/help/community_config_co_2.png" id="modal_image" ><img src="<?=URI_BASE?>assets/img/help/community_config_co_2.png" width="100%" style="max-width: 380px;"></a>
	<p class="padding_top_20">
		画像ではサムネイルの横に37秒前、2時間前と書かれています。これはそのユーザーが前にGame Usersにアクセスした時間を表示しています。この画像の場合は、ケルベロスくんが37秒前にアクセスし、ねこが2時間前にアクセスしたということです。<br><br>デフォルトの設定では24時間以内のアクセスをオンライン扱いし、そのユーザーの最終アクセス時間が表示されるようになっています。最終アクセス時間から24時間が経過した場合は、OFFLINEと表示されます。<br><br>このOFFLINEと表示されるまでの時間をここで設定することができるのです。例えば1時間と設定した場合は、1時間以内のアクセスをオンライン扱いにして、最終アクセスが何分前なのかを表示します。最終アクセスから1時間が過ぎると、OFFLINE扱いになります。<br><br>1～168の間で、好きな時間を設定してください。
	</p>

  </div>

</article>


