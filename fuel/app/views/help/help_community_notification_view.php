<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-phone" aria-hidden="true"></span> アプリで通知を受ける</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">Androidのスマートフォンまたはタブレットを所有している方は、アプリをインストールするとコミュニティから通知を受け取ることができるようになります。アプリを利用した場合は、メールよりも通知を受けられる頻度は高くなります。<br><br><a href="https://play.google.com/store/apps/details?id=org.gameusers.gameusers" target="_blank"><span class="glyphicon glyphicon-link" aria-hidden="true"></span> Game Usersアプリ</a><br><br>アプリをインストールしたら必ずログインを行ってください。初めてログインする端末の場合は、「このデバイスで通知を受けますか？」と尋ねられるのでOKを選んでください。そうするとそのデバイスで通知を受けられるようになります。</p>

	<p class="padding_top_20"><strong>アプリで通知を受けられるケース</strong><br><br>・ 掲示板に誰かが書き込む<br>・ 告知が追加・編集される<br>・ コミュニティの管理者がメンバーに向けて通知の一斉送信を行う</p>

  </div>

</article>


