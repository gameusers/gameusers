    <p class="padding_bottom_20 border_bottom_dashed">Google ChromeまたはFirefoxを使っている方は、ログインして自分のプレイヤーページにアクセスすると、ブラウザから通知を受けられるようになります。<br><br>ブラウザは最新版を使ってください。通常は自動的にアップデートされるので、常に最新版を使っていることになりますが、自動アップデートを切っている場合、通知が受けられない可能性があります。</p>

	<p class="padding_top_20"><strong>ブラウザで通知を受けられるケース</strong><br><br>・ 掲示板に誰かが書き込む<br>・ 告知が追加・編集される<br>・ コミュニティの管理者がメンバーに向けて通知の一斉送信を行う</p>

