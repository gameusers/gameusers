  <div class="panel-heading"><h2 class="uc_bbs_title">ログインする</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">ログインの仕方は2種類あります。まず上部メニューの「ログイン」を押してログインページに移動してください。<br><br>Twitter、Yahoo、Googleなど大手サイトのアカウントを利用する場合は「ログイン1」ボタンを、IDとパスワードでログインする場合は「ログイン2」ボタンを押してください。</p>

    <p class="padding_top_20"><strong>1. 大手サイトのアカウントを利用してログインする</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_login_1.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_login_1.jpg" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">現在、Twitter、Yahoo Japan、Google、Biglobe、livedoor、AOLのアカウントを利用してログインすることができます。<br><br>利用するサイトのボタンを押すと、各サイトのログインページに移動します。移動先のページでIDとパスワードを入力してログインすると、再度Game Users内に戻ってきます。戻ってきたときには、すでにGame Usersへのログインも完了しています。</p>


    <p class="padding_top_20"><strong>2. ID、パスワードでログインする</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/login_2.jpg" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/login_2.jpg" width="100%"></a>
    <p class="padding_top_20">アカウントを作成した場合は、こちらにIDとパスワードを入力してログインしてください。<br><br>他サイトのアカウントを利用してログインしている場合でも、プレイヤーページ内の「基本設定 > アカウント設定」で、IDとパスワードを設定すると、こちらからもログインすることができるようになります。</p>

