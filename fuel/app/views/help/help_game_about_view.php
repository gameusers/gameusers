<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-pencil" aria-hidden="true"></span> 募集掲示板のおすすめ機能</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <p class="padding_bottom_20 border_bottom_dashed">Game Usersの募集掲示板は便利な機能がたくさんあります！</p>


    <p class="padding_top_20"><strong>1. IDを隠すことができる</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_game_1.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_game_1.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">フレンド募集を書き込むときに、コメントに加えて自分のIDも掲載しなければならないですよね。その時、誰でも見れる場所に自分のIDを掲載するのって少し躊躇しませんか？そういった方のために、Game Usersでは画像のようにIDを隠すことができる機能を用意しています。<br><br>この機能を使えば、返信してくれた人にだけIDを公開したり、返信してくれた人の中から公開する相手を選ぶこともできます。この機能はIDだけでなく、部屋番号や入室パスワードなど、無関係な人には公開しない方がいい情報にも使えるので、非常におすすめです。</p>


    <p class="padding_top_20"><strong>2. 募集する期間を設定できる</strong></p>
    <a href="<?=URI_BASE?>assets/img/help/about_game_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/about_game_2.png" width="100%"></a>
    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">期間限定のクエストを一緒にクリアする相手を探す場合などに設定しておくと便利です。通常は期間が過ぎると「締め切りです」などの書き込みをしなければなりませんが、この機能を使うと、一定期間が過ぎると自動的に募集が締め切られます。その際、掲載しているIDや、部屋番号・入室パスワードなども「*****」と表示されるようになるので、自分で削除する必要もありません。<br><br>また募集を締め切るボタンも用意しており、それを押すとワンボタンで募集が締め切られるので、とても楽です。</p>


    <p class="padding_top_20"><strong>3. 返信が来た時にブラウザ・アプリ・メールで通知を受け取ることができる</strong></p>
    <p>自分の募集に返信してくれた人がいても、すぐに気付くことってなかなか難しいですよね。そうなってしまった場合は相手にも悪いし、気付けなかったせいで一緒にプレイできる機会を失ってしまうかもしれません。<br><br>Game Usersの募集掲示板には返信が来たときに、通知でお知らせする機能がついているので、相手に返信するのが遅れてしまったということがなくなります。お互い気持ちよくやりとりをするために、ぜひこの機能を利用してみてください。</p>


