<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-pencil" aria-hidden="true"></span> 募集投稿</h2></div>
  <div class="panel-body" id="bbs_thread_box">

	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_3.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_3.png" width="100%" style="max-width: 609px;"></a>
	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_4.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_4.png" width="100%" style="max-width: 609px;"></a>

    <p class="padding_top_20 padding_bottom_20 border_bottom_dashed">通常の掲示板の機能に加えて、募集掲示板独自の機能も用意されています。<br><br>募集の投稿の仕方について詳しく説明していきます。</p>


	<p class="padding_top_20"><strong>1. まずは募集の種類を選択しましょう</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_5.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_5.png" width="100%" style="max-width: 609px;"></a>
	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">5種類のタイプから選んでください。募集検索にも影響を及ぼす部分なので、適切なものを選択しましょう。<br><br>

		『プレイヤー募集』<br>今日だけ、1度だけ、一緒にイベントを遊んだり、共にクエストのクリアを目指したり、そういった形で短時間一緒にプレイする相手を探す場合に選択してください。長期間、遊ぶ相手を探す場合は、フレンド募集、ギルド・クランメンバー募集を選択してください。<br><br>

		『フレンド募集』<br>お互いのIDを交換しあって、繰り返し遊ぶ相手を見つけたい場合はこちらを選択してください。<br><br>

		『ギルド・クランメンバー募集』<br>ギルドやクランなど、作ったチームに参加してくれる人を募集する場合はこちらを選択してください。<br><br>

		『売買・交換相手募集』<br>ゲーム内のアイテムを売買したり、交換したりする相手を探したい場合はこちらを選択してください。現実のお金が絡む取引は禁止です。<br><br>

		『その他の募集』<br>上記以外の募集はこちらを選んでください。

	</p>


	<p class="padding_top_20"><strong>2. タイトル、ハンドルネーム、コメントを入力する</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_6.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_6.png" width="100%" style="max-width: 609px;"></a>
	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">募集の内容を書き込みましょう。ログインしている場合は、ハンドルネームを入力する欄はありません。プロフィールのハンドルネームが自動的に使われます。</p>


	<p class="padding_top_20"><strong>3. ID・その他の情報を入力する</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_7.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_7.png" width="100%" style="max-width: 609px;"></a>
	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">フレンド募集などでIDを掲載する必要がある場合は、こちらで入力します。画像に表示されている「ID選択」は、ログインしてIDを予め設定で登録している場合にのみ利用できる機能です。チェックボックスで選択するだけでIDが掲載されるため、毎回入力する必要がなくなって便利です。<br><br>その他の情報というのは、例えばゲーム内で待ち合わせ用の部屋を立てた場合、その部屋番号や入室パスワードなどを入力することができる欄です。ID以外にもどんな情報でも掲載することができます。<br><br>誰にでも公開と書かれている部分は、IDやその他の情報をどういった形で公開するか決める選択フォームです。3つの公開方法があって詳しくは以下になります。<br><br>

		『誰にでも公開』<br>このページにアクセスした人なら誰でもID・その他の情報を見ることができます。<br><br>

		『返信者に公開（全員）』<br>ログインして返信したユーザー全員に、自動でID・その他の情報を公開します。<br><br>

		『返信者に公開（選択）』<br>ログインして返信したユーザーの中からID・その他の情報を公開する相手を選べます。

	</p>



	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_8.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_8.png" width="100%" style="max-width: 648px;"></a>

	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">
	画像はフレンド募集で公開条件を「返信者に公開（全員）」にして投稿してみた例です。「ID・情報を公開する」ボタンを押すと相手にIDとその他の情報が公開されます。<br><br>この例の場合は返信者も「両者同意後に公開」を選んでいるので、こちらからもIDが見れません。相手に自分のIDを公開したときに相手のIDも見れるようになります。<br><br>ちなみに返信者も公開条件を選べます。<br><br>

	『誰にでも公開』<br>このページにアクセスした人なら誰でもID・その他の情報を見ることができます。<br><br>

	『募集者に公開』<br>募集者だけにID・その他の情報を公開します。<br><br>

	『両者同意後に公開』<br>募集者があなたに対して自分のID・その他の情報を公開したときに、同時に相手もあなたのID・その他の情報を見れるようになります。

	</p>


	<p class="padding_top_20"><strong>4. 募集期間を設定する</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_9.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_9.png" width="100%" style="max-width: 609px;"></a>
	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">募集をかける期間を設定したい場合はこちらで日数、時間、分を入力してください。最大365日まで設定することが可能です。<br><br>

	この機能は期間限定のクエストを一緒にクリアする相手を探す場合などに設定しておくと便利です。通常は期間が過ぎると「締め切りです」などの書き込みをしなければなりませんが、この機能を使うと、一定期間が過ぎると自動的に募集が締め切られます。その際、掲載しているIDや、部屋番号・入室パスワードなども「*****」と表示されるようになるので、自分で削除する必要もありません。<br><br>

	また募集を投稿後、再度編集する際に「募集を締め切るボタン」が表示されます。それを押すとワンボタンで募集が締め切られるので、とても楽です。</p>


	<p class="padding_top_20"><strong>5. Twitterでツイートする</strong></p>
	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_10.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_10.png" width="100%" style="max-width: 609px;"></a>
	<p class="padding_top_20 padding_bottom_20 border_bottom_dashed">ここでチェックボックスをチェックしておくと、募集の投稿時にTwitterでツイートされます。募集を投稿したことをURL付きで、みんなに知らせることができるので、募集に応じてくれる人も増えるかもしれません。</p>

	<a href="<?=URI_BASE?>assets/img/help/game_recruitment_11.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/game_recruitment_11.png" width="100%" style="max-width: 693px;"></a>
	<p class="padding_top_20">Game Usersで初めてTwitterの機能を使う方は、こちらの画面に移動します。Twitterのユーザー名とパスワードを入力してTwitterにログインすると、Game Usersに再度戻ってきて募集の投稿が完了します。<br><br>Game Usersにログインする際にTwitterのアカウントを利用している方はこの画面は出ずに、スムーズに投稿が完了します。</p>


