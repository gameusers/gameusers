<article class="panel panel-info">

  <div class="panel-heading"><h2 class="uc_bbs_title"><span class="glyphicon glyphicon-th-large" aria-hidden="true"></span> ブラウザで通知を受ける</h2></div>
  <div class="panel-body" id="bbs_thread_box">

    <a href="<?=URI_BASE?>assets/img/help/community_notification_2.png" id="modal_image"><img src="<?=URI_BASE?>assets/img/help/community_notification_2.png" width="100%" style="max-width: 362px;" class="padding_bottom_20"></a>
    <p class="padding_bottom_20 border_bottom_dashed">Google ChromeまたはFirefoxを使っている方は、ログインして自分のプレイヤーページにアクセスすると、ブラウザから通知を受けられるようになります。<br><br>ブラウザは最新版を使ってください。通常は自動的にアップデートされるので、常に最新版を使っていることになりますが、自動アップデートを切っている場合、通知が受けられない可能性があります。<br><br><a href="javascript:void(0)" class="bbs_list_title" onclick="GAMEUSERS.common.readHelp(this, 1, null, 'player_notification_browser')"><span class="glyphicon glyphicon-question-sign" aria-hidden="true"></span> ブラウザで通知を受ける方法</a></p></p>

  <p class="padding_top_20"><strong>ブラウザで通知を受けられるケース</strong><br><br>・ 募集掲示板に新たな募集が投稿される<br>・ 募集掲示板の自分の投稿に返信が書き込まれる<br>・ 参加しているコミュニティの掲示板に誰かが書き込む<br>・ 参加しているコミュニティの告知が追加・編集される<br>・ 参加しているコミュニティの管理者がメンバーに向けて通知の一斉送信を行う</p>

  </div>

</article>


